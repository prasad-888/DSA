import java.util.*;

public class unary {
    public static void main(String args[]){
        //Preincrement
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);
        //Output 
        //11
        //11



        //postincrement
        int c = 10;
        int d = c++;
        System.out.println(c);
        System.out.println(d);
        //output
        //11
        //10


        //predecrement
        int m=10;
        int n= --m;
        System.out.println(m);
        System.out.println(n);
        //Output
        //9
        //9

        //postdecrement
        int p =10;
        int q = p--;
        System.out.println(p);
        System.out.println(q);
        //Output
        //9
        //10



    }
}
